function normalizeKey(key: string) {
  const parts = key
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim()
    .split(/\s+/)
    .filter(Boolean)
  if (parts.length === 0) return ''
  return parts[0].toLowerCase() + parts.slice(1).map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase()).join('')
}

export function translate(lang: Language, key: string) {
  const normalized = normalizeKey(key)
  return translations[lang]?.[key] ?? translations[lang]?.[normalized] ?? translations.en[key] ?? translations.en[normalized] ?? key
}
